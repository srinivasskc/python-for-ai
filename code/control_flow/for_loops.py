# Python starts counting from zero
for i in range(5):
    print(i)


# prints Hello,World - 5 times.
for i in range(5):
    print("Hello, World")


# Count from 1 to 5
for i in range(1,6):
    print(i)

# count by 2 steps (start,stop,step)
for i in range(0,10,2):
    print(i)

# Loop through text

name = "Srinivas"

# prints each letter in new line
for letter in name:
    print(letter)
